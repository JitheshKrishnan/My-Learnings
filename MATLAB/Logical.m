clc;
clear all;
close all;
a = zeros(256,256);
for x = 50:100;
    for y = 50:100;
        a(x,y)=1;
    end;
end;
b = zeros(256,256);
for x = 100:200;
    for y = 100:200;
        b(x,y)=1;
    end;
end;
subplot(321);
imshow(a);
title('a');
subplot(322);
imshow(b);
title('b');
subplot(323);
p = bitand(a,b);
imshow(p);
title('and');
subplot(323);
q = bitor(a,b);
imshow(q);
title('or');
subplot(324);
r = bitcmp(a);
imshow(r);
title('not a');
subplot(325);
s = bitcmp(b);
imshow(s);
title('not b');
subplot(326);
t = bitxor(a,b);
imshow(t);
title('xor');