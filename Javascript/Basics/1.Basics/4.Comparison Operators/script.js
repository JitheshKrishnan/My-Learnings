let apples = 10;
let oranges = "10";

// ! ==
let isEqual = apples == oranges;
console.log(isEqual);

// ! ===
let isStrictlyEqual = apples === oranges;
console.log(isStrictlyEqual);
let isStrictlyNotEqual = apples !== oranges;
console.log(isStrictlyNotEqual);

// ! >
let isGreaterThan = apples > oranges;

// ! <
let isLessThan = apples < oranges;

// ! >=
let isGreaterThanOrEqual = apples >= oranges;

// ! <=
let isLessThanOrEqual = apples <= 10;
